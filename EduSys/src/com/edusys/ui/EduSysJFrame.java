    private void bntThoatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntThoatActionPerformed
        int i = JOptionPane.showConfirmDialog(this, "bạn có muốn thoát khỏi chương trình không");
        if (i == JOptionPane.YES_OPTION) {
            try {
                cnt.close();
            } catch (SQLException ex) {
                Logger.getLogger(EduSysJFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            System.exit(0);
        }
    }//GEN-LAST:event_bntThoatActionPerformed

