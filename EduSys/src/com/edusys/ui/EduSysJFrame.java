    private void clock() {
        Timer time = new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent ae) {
                SimpleDateFormat sdf = new SimpleDateFormat("hh:mm:ss a");
                Date date = new Date();
                lblDongHo.setText(sdf.format(date.getTime()));
            }
        });
        time.start();
    }

