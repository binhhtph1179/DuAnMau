    private boolean validateForm() {
        if (txtMaNguoiHoc.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "không được để trống mã người học");
            txtMaNguoiHoc.requestFocus();
            return false;
        }
        if (txtHoTen.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "không được để trống Họ Tên người học");
            txtHoTen.requestFocus();
            return false;
        }
        if (!rdoNam.isSelected() && !rdonu.isSelected()) {
            JOptionPane.showMessageDialog(this, "chưa chọn giới tính");
            return false;
        }
        if (txtDienThoai.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống Số điện thoại");
            txtDienThoai.requestFocus();
            return false;
        }
        if (txtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống Email");
            txtEmail.requestFocus();
            return false;
        }

        return true;
    }

