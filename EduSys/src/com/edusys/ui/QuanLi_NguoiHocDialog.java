    private void fillToTable() {
        model.setRowCount(0);
        for (NguoiHoc nh : ListNguoiHoc) {
            if (nh.isDeleteAt() == true) {
                model.addRow(new Object[]{nh.getMaHocVien(),
                    nh.getHotenHocVien(),
                    nh.getGiuoiTinh(),
                    nh.getNgaySinh(),
                    nh.getDienThoai(),
                    nh.getEmail(),
                    nh.getMaNhanVien(),
                    nh.getNgayDK()});
            }
        }
    }

    private NguoiHoc getNguoiHoc() {
        NguoiHoc nh = new NguoiHoc();
        nh.setMaHocVien(txtMaNguoiHoc.getText());
        nh.setHotenHocVien(txtHoTen.getText());
        nh.setDienThoai(txtDienThoai.getText());
        nh.setGhiChu(txtghiChu.getText());
        nh.setEmail(txtEmail.getText());
        nh.setMaNhanVien(loginJDialog.txtTaiKhoan.getText());
        nh.setDeleteAt(true);
        nh.setNgayDK(new java.sql.Date(date.getTime()));
        nh.setNgaySinh(txtNgaySinh.getDate());
        String gt = "";
        if (rdoNam.isSelected()) {
            gt = "Nam";
        }
        if (rdonu.isSelected()) {
            gt = "Nữ";
        }
        nh.setGiuoiTinh(gt);
        return nh;
    }

