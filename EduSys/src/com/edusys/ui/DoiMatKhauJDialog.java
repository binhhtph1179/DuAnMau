    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        NhanVien nv = nvDAO.selectById(txtTaiKhoan.getText());
        if (!nv.getMatkhau().equals(txtMatKhauCu.getText())) {
            JOptionPane.showMessageDialog(this, "sai mật khẩu");
            txtMatKhauCu.requestFocus();
        } else {
            if (txtMatKhauMoi.getText().equals(txtNhapLai.getText())) {
                doiMatKhau();
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "mật khẩu nhập chưa trùng khớp");
                txtNhapLai.requestFocus();
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

