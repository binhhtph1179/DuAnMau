    private void doiMatKhau() {
        NhanVien nv = new NhanVien();
        nv.setManhanvien(txtTaiKhoan.getText());
        nv.setMatkhau(txtNhapLai.getText());
        try {
            nvDAO.capNhatMatKhau(nv);
            JOptionPane.showMessageDialog(this, "đổi mật khẩu thành công");
        } catch (Exception e) {
        }
    }
